# Crear un diccionario con informaci√≥n de una persona {clave:valor}
persona = {
    'nombre' : 'Catalina',
    'edad' : 33,
    'ciudad' : 'Bogota'
}

# Acceder a los elementos usando corchetes
nombre = persona['nombre']
edad = persona['edad']
ciudad = persona['ciudad']

# Imprimir los valores
print('Nombre: ', nombre)
print('Edad: ', edad)
print('Ciudad: ', ciudad)


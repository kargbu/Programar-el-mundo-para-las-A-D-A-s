# Contador de números específicos
# Escribe un programa que cuente cuántas veces aparece un número específico en una lista.

lista_num = input("Ingresa una lista de números ")
buscar = input("¿qué número quieres buscar? ")
a = f"El número {buscar} a parece {lista_num.count(buscar)} vez o veces en la lista."

print(a)


